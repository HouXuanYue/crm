     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        // dd($data);
        $res = DB::table('supplier')->insert($data);
        // dd($res);
        if($data){
            return ['code'=>1,'msg'=>'添加成功'];
        }else{
            return ['code'=>2,'msg'=>'添加失败'];
        }
    }

